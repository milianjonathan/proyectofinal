class FondoPanel extends JPanel{
    public Image imagen;
    @Override
    public void paint(Graphics g){
       try{
        imagen = new ImageIcon(getClass().getResource("/AJFVCim치genesvarias/fondo.jpg")).getImage();
       }catch(NullPointerException e){
           
       }
        g.drawImage(imagen, 0, 0, getWidth(), getHeight(), this);
        setOpaque(false);
        super.paint(g);
    }
}

